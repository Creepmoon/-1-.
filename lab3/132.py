import numpy as np

# Исходная матрица A
A = np.array([
    [1, 0, -3, 1, 0, -3],
    [0, 3, -3, -1, -2, 3],
    [2, -2, -3, 4, 0, 3],
    [-4, -2, 1, -3, -1, 3],
    [3, 3, 1, 2, 1, -1],
    [-4, 0, -2, -2, -3, 3],
    [1, 2, -3, 0, -2, -1],
    [-3, -3, 3, 0, 4, 3],
    [3, 0, 1, -3, 3, 2]
])

# Формируем трехмерный массив (тензор)
A_reshaped = A.reshape(3, 3, 6)

# Вычисляем c_l
c_l = np.einsum('klk->l', A_reshaped)

# Приводим результат к требуемому виду
result = c_l.flatten().tolist()
print(result)
